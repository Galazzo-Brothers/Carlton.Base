namespace Carlton.Core.Components.Toasts;

/// <summary>
/// Enum representing different types of toast messages.
/// </summary>
public enum ToastTypes
{
    /// <summary>
    /// Represents a success toast message.
    /// </summary>
    Success = 1,

    /// <summary>
    /// Represents an information toast message.
    /// </summary>
    Info = 2,

    /// <summary>
    /// Represents a warning toast message.
    /// </summary>
    Warning = 3,

    /// <summary>
    /// Represents an error toast message.
    /// </summary>
    Error = 4,
}