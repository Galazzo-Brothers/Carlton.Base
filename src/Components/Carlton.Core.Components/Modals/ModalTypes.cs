namespace Carlton.Core.Components.Modals;

/// <summary>
/// Represents the types of modals available.
/// </summary>
public enum ModalTypes
{
    /// <summary>
    /// Represents a modal with a single action.
    /// </summary>
    SingleActionModal = 1,

    /// <summary>
    /// Represents a confirmation modal that requires user confirmation.
    /// </summary>
    ConfirmationModal = 2,
}